void solve() {
    int n; cin >> n;
    string s;
    cin >> s;
    int ans=0,open=0;
    for(char c:s){
        if(c==')'){
            if(open>0) open--;
            else ans++;
        }
        else open++;
    }
    cout << ans << endl;
}
